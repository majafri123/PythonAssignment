# 3. Create Lambda function to find the cube of a user define number

cube = lambda a : a*a*a
print("cube is: ",cube(int(input("enter number to find cube"))))

# 4. Create Lambda function to find the multiplication of two user define numbers

multi = lambda a, b: a*b
print("Multiplication of two numbers are: ",multi(int(input("enter first number")),int(input("enter second number"))))